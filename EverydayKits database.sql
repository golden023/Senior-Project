CREATE TABLE Orders(OrderID INT(6) PRIMARY KEY NOT NULL,
					CustomerID INT(5) NOT NULL,
                    DateCreated TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                    FOREIGN KEY (CustomerID) REFERENCES Customers(CustomerID));
			
CREATE TABLE Invoices(InvoiceID INT(7) PRIMARY KEY NOT NULL,
						CustomerID INT(5) NOT NULL,
                        InvoiceDate TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                        PaymentTotal DOUBLE(10,2) NOT NULL,
                        OrderID INT(6) NOT NULL,
                        FOREIGN KEY (OrderID) REFERENCES Orders(OrderID));
                        
CREATE TABLE ShippingInformation(ShippingID INT(8) PRIMARY KEY NOT NULL,
									OrderID INT(6) NOT NULL,
                                    InvoiceID INT(7) NOT NULL,
                                    TrackingID INT(20) NOT NULL,
                                    ShipDate DATETIME NOT NULL,
                                    ShipAddress NVARCHAR(30) NOT NULL,
                                    FOREIGN KEY (OrderID) REFERENCES Orders(OrderID));
                                    
CREATE TABLE Payments(PaymentID INT(9) PRIMARY KEY NOT NULL,
						CustomerID INT(5) NOT NULL,
                        CardNumber INT(16) NOT NULL,
                        FOREIGN KEY (CustomerID) REFERENCES Customers(CustomerID));
                        
CREATE TABLE KitDetails(KitID INT(3) NOT NULL,
						ProductID INT(4) NOT NULL,
						ProductName NVARCHAR(50) NOT NULL,
                        KitName NVARCHAR(50) NOT NULL,
                        FOREIGN KEY (KitID) REFERENCES Kits(KitID),
						FOREIGN KEY (ProductID) REFERENCES Products(ProductID));
                        

      
